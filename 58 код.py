import math

# Исходные данные
S = 124.18  # расстояние, м
m_S = 0.030  # погрешность расстояния, м
Z_deg = 70 + 18 / 60 + 30 / 3600  # зенитное расстояние в градусах
m_Z_sec = 40  # погрешность зенитного расстояния в секундах

# Перевод угла Z в радианы
Z_rad = math.radians(Z_deg)  # зенитное расстояние в радианах
m_Z_rad = math.radians(m_Z_sec / 3600)  # погрешность угла в радианах

# Вычисление высоты сигнала U
U = S * (1 / math.tan(Z_rad))

# Частные производные
dU_dS = 1 / math.tan(Z_rad)  # частная производная по S
dU_dZ = -S / (math.sin(Z_rad) ** 2)  # частная производная по Z

# Вычисление средней квадратической погрешности m_U
m_U = math.sqrt((dU_dS * m_S) ** 2 + (dU_dZ * m_Z_rad) ** 2)

# Вывод результатов
print(f"Высота сигнала (U): {U:.2f} м")
print(f"Средняя квадратическая погрешность (m_U): {m_U:.3f} м")
