import math

S = 124.18  # Расстояние в метрах
m_S = 0.030  # Погрешность расстояния
Z_deg = 70 + 18/60 + 30/3600  # Зенитное расстояние в градусах
m_Z = 40 / 3600  # Погрешность угла в радианах (переводим из секунд)

# Переводим угол в радианы
Z_rad = Z_deg * math.pi / 180

# Вычисляем высоту U
U = S * (1 / math.tan(Z_rad))  # cot(Z) = 1 / tan(Z)

# Частные производные
dU_dS = 1 / math.tan(Z_rad)  # cot(Z)
dU_dZ = -S * (1 / math.sin(Z_rad))**2  # -S * csc^2(Z)

# Средняя квадратическая погрешность U
m_U = math.sqrt((dU_dS * m_S)**2 + (dU_dZ * m_Z)**2)

print(f"Вычисленная высота сигнала U = {U:.3f} м")
print(f"Средняя квадратическая погрешность высоты сигнала m_U = {m_U:.3f} м")