import math

h = 0.5  # Высота сечения в метрах
l = 2.4  # Расстояние на карте в см
d = 5.7  # Величина заложения в см

# Погрешности
m_l = 0.1  # Погрешность расстояния l в см
m_d = 0.2  # Погрешность заложения d в см
m_H = 10   # Погрешность младшей горизонтали H в см

# Частные производные
dH_dl = h / d
dH_dd = -h * l / d**2
dH_dh = l / d

# Средняя квадратическая погрешность
m_H_result = math.sqrt((dH_dl * m_l)**2 + (dH_dd * m_d)**2 + (dH_dh * m_H)**2)

print(f"Средняя квадратическая погрешность определения отметки: {m_H_result:.2f} см")