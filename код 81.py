import math

L1 = 3  # Длина первой секции в км
L2 = 5  # Длина второй секции в км
m_H1 = 4  # Погрешность начальной отметки в мм
m_H_km = 2  # Погрешность на 1 км хода в мм/км

# Погрешности, связанные с длиной секций
m_L1 = L1 * m_H_km
m_L2 = L2 * m_H_km

# Средняя квадратическая погрешность
m_H = math.sqrt(m_H1**2 + m_L1**2 + m_L2**2)

print(f"Средняя квадратическая погрешность третьей отметки: {m_H:.2f} мм")